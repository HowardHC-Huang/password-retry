##密碼重試程式##

#1.先設定好密碼
#2.讓使用者最多輸入3次密碼
#3.不對:印出"密碼錯誤, 還有_次機會"
#4.對:印出"登入成功"


password = 'a123456'
#answer = input('請輸入密碼 \n')		#這行寫進迴圈較好

n = 3
while n > 0 :
	answer = input('請輸入密碼 \n')
	if answer == password:
		print('登入成功!')
		break
	else:
		n = n - 1
		if n <= 0:
			print('嘗試次數過多, 終止嘗試')
		else:
			print('密碼錯誤,還有', n, '次機會')


